    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(PidTf.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing information");
        }else{
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clothes?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "lolbiglol");
                String pid = PidTf.getText();
                String Query = "Delete from items where IID=" + pid;
                Statement delete = con.createStatement();
                delete.executeUpdate(Query);
                JOptionPane.showMessageDialog(this, "Item Deleted");
                diplayItems();
                reset();
            } catch (SQLException ex) {
                Logger.getLogger(Clothes.class.getName()).log(Level.SEVERE, null, ex);
            }
               
            
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

