    private void diplayItems(){
        try {
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clothes?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "lolbiglol");
            st = con.createStatement();
            rs = st.executeQuery("select * from items");
            ItemsTbl.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException ex) {
            Logger.getLogger(Clothes.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void reset(){
        PidTf.setText("");
        NameTf.setText("");
        CatgCb.setSelectedIndex(0);
        SizeCb.setSelectedIndex(0);
        QunTf.setText("");
        PriceTf.setText("");
    }
    
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
        if(PidTf.getText().isEmpty() || NameTf.getText().isEmpty() || CatgCb.getSelectedIndex()==-1 || SizeCb.getSelectedIndex()==-1 || QunTf.getText().isEmpty() || PriceTf.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing information");
        }else{
            
            try {
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/clothes?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "lolbiglol");
                
                PreparedStatement add = con.prepareStatement("insert into items values(?,?,?,?,?,?)");
                add.setInt(1, Integer.valueOf(PidTf.getText()));
                add.setString(2, NameTf.getText());
                add.setString(3, CatgCb.getSelectedItem().toString());
                add.setInt(4, Integer.valueOf(PriceTf.getText()));
                add.setInt(5, Integer.valueOf(QunTf.getText()));
                add.setString(6, SizeCb.getSelectedItem().toString());
                int row = add.executeUpdate();
                JOptionPane.showMessageDialog(this, "Item Added");
                diplayItems();
                reset();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "You may entered ID which already exists");
                Logger.getLogger(Clothes.class.getName()).log(Level.SEVERE, null, ex);
            }
                
            
        }
    }//GEN-LAST:event_AddBtnMouseClicked

